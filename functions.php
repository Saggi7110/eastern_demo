function handle_user_import() {
    if ( isset( $_POST['submit'] ) ) {
        if ( ! empty( $_FILES['csv_file']['name'] ) ) {
            $uploaded_file = $_FILES['csv_file'];

            // Check if the file is a CSV
            $file_type = wp_check_filetype( $uploaded_file['name'], ['csv' => 'text/csv'] );
            if ( $file_type['ext'] === 'csv' && $file_type['type'] === 'text/csv' ) {

                // Set the upload directory
                $upload_dir = wp_upload_dir();
                $upload_path = $upload_dir['path'] . '/' . $uploaded_file['name'];

                // Move the uploaded file to the destination
                move_uploaded_file( $uploaded_file['tmp_name'], $upload_path );

                // Process the CSV file (You'll implement this part in the next steps)
                process_csv_file( $upload_path );

                // Display a success message
                echo '<div class="updated notice"><p>Users imported successfully!</p></div>';

            } else {
                // Display an error message if the file is not a CSV
                echo '<div class="error notice"><p>Please upload a valid CSV file.</p></div>';
            }
        }
    }
}

add_action( 'admin_init', 'handle_user_import' );

function process_csv_file($file_path) {
    // Check if the file exists
    if (file_exists($file_path)) {
        // Read CSV file content
        $csv_data = array_map('str_getcsv', file($file_path));

        // Check if the CSV has headers and skip the first row
        $headers = array_shift($csv_data);

        // Loop through each row of data
        foreach ($csv_data as $user_data) {
            // Extract user details
            $username = !empty($user_data[0]) ? $user_data[0] : '';
            $email = !empty($user_data[1]) ? $user_data[1] : '';
            $password = !empty($user_data[2]) ? $user_data[2] : '';

            // Check if the username and email are not empty
            if (!empty($username) && !empty($email)) {
                // Create user data array with 'subscriber' role
                $user_data = array(
                    'user_login' => $username,
                    'user_email' => $email,
                    'user_pass' => $password,
                    'role' => 'subscriber',
                );

                // Insert the user into the database
                $user_id = wp_insert_user($user_data);

                // Check if the user was created successfully
                if (!is_wp_error($user_id)) {
                    // User created successfully
                    echo '<div class="updated notice"><p>User ' . $username . ' imported successfully with ID: ' . $user_id . '!</p></div>';
                } else {
                    // Error creating user
                    echo '<div class="error notice"><p>Error importing user ' . $username . ': ' . $user_id->get_error_message() . '</p></div>';
                }
            }
        }
    }
}




